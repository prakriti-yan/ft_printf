#include "libft.h"

void	ft_putunbr(unsigned long long n)
{
	if (n >= 10)
	{
		ft_putunbr(n / 10);
		ft_putunbr(n % 10);
	}
	else if (n >= 0 && n < 10)
		ft_putchar(n + '0');
}
